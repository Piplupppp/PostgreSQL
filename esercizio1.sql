-- Creazione tipi ENUM per tabelle noleggio e sconto
CREATE TYPE stato_restituzione_enum AS ENUM ('Buono', 'Danneggiato');
CREATE TYPE descrizione_sconto_enum AS ENUM ('5 noleggi', '10 noleggi', '25 noleggi', '50 noleggi');
CREATE TYPE stato_noleggio_enum AS ENUM ('Attivo', 'Chiuso', 'Prenotato');

CREATE TABLE store (
    store_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    localita VARCHAR(255) NOT NULL
);
 
CREATE TABLE fornitore (
    fornitore_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(50) NOT NULL,
    telefono VARCHAR(20) NOT NULL,
    email VARCHAR(50) NOT NULL
);
 
CREATE TABLE staff (
    staff_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,    
    ruolo VARCHAR(15) NOT NULL, -- 'responsabile' o 'dipendente'
    nome VARCHAR(50) NOT NULL,
    cognome VARCHAR(50) NOT NULL,
    matricola INT UNIQUE NOT NULL
);
 
CREATE TABLE cliente (
    cliente_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(50) NOT NULL,
    cognome VARCHAR(50) NOT NULL,
    data_nascita DATE NOT NULL,
    telefono VARCHAR(20) NOT NULL,
    cellulare VARCHAR(20) NOT NULL,
    email VARCHAR(50) UNIQUE NOT NULL,
    indirizzo VARCHAR(50) NOT NULL,
    id_liberatoria VARCHAR(50) NOT NULL,
    data_firma_liberatoria DATE NOT NULL
);
 
CREATE TABLE video (
    video_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titolo VARCHAR(50) NOT NULL,
    nome_regista VARCHAR(50) NOT NULL,
    cognome_regista VARCHAR(50) NOT NULL,
    casa_produttrice VARCHAR(50) NOT NULL,
    durata_min INT NOT NULL,
    genere VARCHAR(15) NOT NULL
);
 
CREATE TABLE attore (
    attore_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(50) NOT NULL,
    cognome VARCHAR(50) NOT NULL
);

-- Per relazione molti a molti, in tabella intermedia aggiungo direttamente le FKs, perché la creo dopo aver creato le altre due tabelle "madri"
CREATE TABLE video_attore (
    video_id INT NOT NULL REFERENCES video(video_id),
    attore_id INT NOT NULL REFERENCES attore(attore_id),
    PRIMARY KEY (video_id, attore_id)
);
 
-- Copia fisica singola di un video, presente in un negozio, arrivata da un fornitore
CREATE TABLE copia_video (
    copia_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,    
    data_carico_fornitore DATE NOT NULL,
	data_ritiro_fornitore DATE
);
 
CREATE TABLE tariffa (
    tariffa_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    prezzo_giorno DECIMAL(5,2) NOT NULL,
    gg_min SMALLINT NOT NULL,
    gg_max SMALLINT NOT NULL
);
 
CREATE TABLE sconto (
    sconto_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    descrizione descrizione_sconto_enum NOT NULL,
    percentuale_sconto DECIMAL(5,2) NOT NULL
);

CREATE TABLE noleggio (
    noleggio_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,    
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    data_restituzione DATE, -- NULL finché non rientra
    stato_restituzione stato_restituzione_enum, -- 'buono' / 'danneggiato', nullable perché rivalutato ogni volta al rientro
    stato_noleggio stato_noleggio_enum, -- 'attivo', 'chiuso', 'prenotato'
	importo_pagato DECIMAL(6,2) NOT NULL
);

CREATE TABLE incentivo (
    incentivo_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    periodo DATE NOT NULL,
    soglia_raggiunta INT NOT NULL,
    importo DECIMAL(5,2) NOT NULL
);



