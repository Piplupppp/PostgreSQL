-- Stato del catalogo e calcolo disponibilità copie in tempo reale per genere e titolo
SELECT 
    video.titolo,
    video.genere,
    store.localita AS "Negozio",
    COUNT(copia_video.copia_id) AS "Copie Totali Assegnate",
    COUNT(copia_video.copia_id) FILTER (
        WHERE copia_video.data_ritiro_fornitore IS NULL 
        AND copia_video.copia_id NOT IN (
            SELECT noleggio.copia_id 
            FROM noleggio 
            WHERE noleggio.stato_noleggio = 'Attivo'
        )
    ) AS "Copie Disponibili Ora"
FROM video
JOIN copia_video ON video.video_id = copia_video.video_id
JOIN store ON copia_video.store_id = store.store_id
-- WHERE video.genere = 'Fantascienza' -- per ricerca sul catalogo per genere
-- WHERE video.titolo ILIKE '%Matrix%' -- per ricerca sul catalogo per titolo
GROUP BY video.titolo, video.genere, store.localita
ORDER BY video.titolo, store.localita;


-- Calcolo dell'incasso giornaliero per singolo punto vendita e per addetto
SELECT 
    store.localita AS "Negozio",
    staff.nome AS "Nome Addetto",
    staff.cognome AS "Cognome Addetto",
    SUM(noleggio.importo_pagato) AS incasso_totale_giornaliero
FROM noleggio
JOIN staff ON noleggio.staff_id = staff.staff_id
JOIN store ON staff.store_id = store.store_id
WHERE noleggio.start_date = CURRENT_DATE -- Calcolato sul giorno corrente ma modificabile
GROUP BY store.localita, staff.staff_id, staff.nome, staff.cognome
ORDER BY store.localita, incasso_totale_giornaliero DESC;


-- Calcolo del volume mensile dei noleggi per addetto per la verifica del raggiungimento soglie incentivi
SELECT 
    staff.staff_id,
    staff.nome,
    staff.cognome,
    store.localita AS "Negozio",
    COUNT(noleggio.noleggio_id) AS numero_noleggi_mese_corrente
FROM staff
JOIN store ON staff.store_id = store.store_id
LEFT JOIN noleggio
    ON staff.staff_id = noleggio.staff_id
   AND EXTRACT(MONTH FROM noleggio.start_date) = EXTRACT(MONTH FROM CURRENT_DATE)
   AND EXTRACT(YEAR FROM noleggio.start_date) = EXTRACT(YEAR FROM CURRENT_DATE)
GROUP BY staff.staff_id, staff.nome, staff.cognome, store.localita
ORDER BY numero_noleggi_mese_corrente DESC;


-- Storico noleggi per un cliente specifico
SELECT 
    noleggio.noleggio_id,
    video.titolo,
    noleggio.start_date,
    noleggio.end_date,
    noleggio.data_restituzione,
    noleggio.stato_noleggio,
    noleggio.importo_pagato
FROM noleggio
JOIN copia_video ON noleggio.copia_id = copia_video.copia_id
JOIN video ON copia_video.video_id = video.video_id
-- WHERE noleggio.cliente_id = 1 -- sostituisci con ID del cliente da visualizzare
ORDER BY noleggio.start_date DESC; -- in ordine decrescente


-- Calcolo scadenza ritiro fornitore
SELECT
    copia_video.copia_id,
    video.titolo,
    fornitore.nome AS Fornitore,
    store.localita AS Negozio,
    copia_video.data_carico_fornitore,
    CAST(copia_video.data_carico_fornitore + INTERVAL '90 days' AS DATE) AS data_scadenza_fornitore, -- creo colonna calcolandola, perché non esiste già in db, e CAST per togliere orario
    (copia_video.data_carico_fornitore + INTERVAL '90 days') <= CURRENT_DATE AS "Scaduta"
FROM copia_video
JOIN video ON video.video_id = copia_video.video_id
JOIN fornitore ON fornitore.fornitore_id = copia_video.fornitore_id
JOIN store ON store.store_id = copia_video.store_id
-- WHERE copia_video.data_ritiro_fornitore IS NULL -- mostra solo quelle non scadute
ORDER BY data_scadenza_fornitore;


-- EXTRA: elenco prenotazioni (titoli prenotati dai clienti per un ritiro futuro)
SELECT 
    noleggio.noleggio_id,
    cliente.nome AS nome_cliente,
    cliente.cognome AS cognome_cliente,
    cliente.telefono,
    video.titolo,
    store.localita AS negozio_ritiro,
    noleggio.start_date AS data_ritiro_prevista,
    noleggio.end_date AS data_restituzione_prevista
FROM noleggio
JOIN cliente ON noleggio.cliente_id = cliente.cliente_id
JOIN copia_video ON noleggio.copia_id = copia_video.copia_id
JOIN video ON copia_video.video_id = video.video_id
JOIN store ON copia_video.store_id = store.store_id
WHERE noleggio.stato_noleggio = 'Prenotato'
ORDER BY noleggio.start_date ASC; -- ordinato dal ritiro più imminente a quello più lontano


-- EXTRA: report dei video restituiti danneggiati con calcolo importo rimborsi da emettere verso il fornitore
SELECT 
    noleggio.noleggio_id,
    video.titolo,
    fornitore.nome AS "Fornitore",
    cliente.cognome AS "Cognome Cliente",
    noleggio.data_restituzione,
    noleggio.importo_pagato AS "Quota Noleggio",
    -- Esempio di calcolo penale: 3 volte il prezzo base della tariffa giornaliera associata
    (tariffa.prezzo_giorno * 3) AS "Addebito Danno Cliente"
FROM noleggio
JOIN copia_video ON noleggio.copia_id = copia_video.copia_id
JOIN video ON copia_video.video_id = video.video_id
JOIN fornitore ON copia_video.fornitore_id = fornitore.fornitore_id
JOIN cliente ON noleggio.cliente_id = cliente.cliente_id
JOIN tariffa ON noleggio.tariffa_id = tariffa.tariffa_id
WHERE noleggio.stato_restituzione = 'Danneggiato'
ORDER BY noleggio.data_restituzione DESC;